    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        
        //    list = OnlineList.selectedList;
          //  list2 = OnlineList.Notgrp;
            int temp[]= membersList.getSelectedIndices();
        
            if(temp.length != 0)
            {
               deletedMembersList = new ArrayList<String>();
            
//            if(temp.length < OnlineList.selectedList.size())
//            {
                for(int i=0;i<temp.length;i++)
                {
                   NotInGroupList.add(selectedList.get(temp[i]));
                   deletedMembersList.add(selectedList.get(temp[i]));
                }
                Iterator<String> it = selectedList.iterator();
                while(it.hasNext())
                {
                    if(NotInGroupList.contains(it.next()))
                        it.remove();
                }
                membersList.setListData(removeUserFromPreview(selectedList).toArray(new String[(removeUserFromPreview(selectedList).size())]));
                MyChatWindow.setGroupChatSelectedList(selectedList);
                MyChatWindow.setGroupChatNotSelectedList(NotInGroupList);
                OnlineList.Sconnect.removeUser(deletedMembersList.toArray(new String[(deletedMembersList.size())]), MyChatWindow.getId());
            }
            else
            {
                JOptionPane.showMessageDialog(null,"please choose a member to remove!");
            }
    }//GEN-LAST:event_deleteButtonActionPerformed

