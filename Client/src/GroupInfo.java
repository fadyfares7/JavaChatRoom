    private ArrayList<String> removeUserFromPreview(ArrayList<String> p)
    {
        ArrayList<String> previewList = p;
        for(int i =0; i < p.size(); i++ )
        {
            if(p.get(i).equals(Main.username))
                previewList.remove(i);
        }
        return previewList;
    }
    
